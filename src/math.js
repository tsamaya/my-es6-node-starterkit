/**
 * sum operation
 * @param  {Number} a number value
 * @param  {Number} b number value
 * @return {Number}   number value (a+b)
 */
const sum = (a, b) => Number(a) + Number(b);

/**
 * diff operation
 * @param  {Number} a number value
 * @param  {Number} b number value
 * @return {Number}   number value (a-b)
 */
const diff = (a, b) => Number(a) - Number(b);

/**
 * export functions
 */
export { sum, diff };
